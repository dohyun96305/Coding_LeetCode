# PRODUCT ONLY SOLD IN FIRSRT QUATER OF 2019 
# 2019-01-01 ~ 2019-03-31

SELECT A.PRODUCT_ID, A.PRODUCT_NAME 
FROM PRODUCT AS A 
JOIN SALES AS B ON A.PRODUCT_ID = B.PRODUCT_ID
GROUP BY PRODUCT_ID
HAVING MIN(SALE_DATE) >= '2019-01-01' AND 
    MAX(SALE_DATE) <= '2019-03-31'